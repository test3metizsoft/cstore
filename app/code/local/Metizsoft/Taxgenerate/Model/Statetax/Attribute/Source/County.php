/**
 * Admin source model for County
 *
 * @category    Metizsoft
 * @package     Metizsoft_Taxgenerate
 * @author      Metizsoft Solutions<http://metizsoft.com>
 */
class Metizsoft_Taxgenerate_Model_Statetax_Attribute_Source_County extends Mage_Eav_Model_Entity_Attribute_Source_Table
{
    /**
     * get possible values
     *
     * @access public
     * @param bool $withEmpty
     * @param bool $defaultValues
     * @return array
     * @author Metizsoft Solutions
     */
    public function getAllOptions($withEmpty = true, $defaultValues = false)
    {
        $counties = Mage::getModel('metizsoft_taxgenerate/countylist')->getCollection();
        $counties->load();
        $countyarray = array();
        $i=0;
        foreach ($counties as $county) {
           $countyarray[$i]['label'] = Mage::helper('metizsoft_taxgenerate')->__($county->getName());
           $countyarray[$i]['value'] = $county->getEntityId();
           $i++;
        }
        $options =  $countyarray;
        if ($withEmpty) {
            array_unshift($options, array('label'=>'', 'value'=>''));
        }
        return $options;

    }

    /**
     * get options as array
     *
     * @access public
     * @param bool $withEmpty
     * @return string
     * @author Metizsoft Solutions
     */
    public function getOptionsArray($withEmpty = true)
    {
        $options = array();
        foreach ($this->getAllOptions($withEmpty) as $option) {
            $options[$option['value']] = $option['label'];
        }
        return $options;
    }

    /**
     * get option text
     *
     * @access public
     * @param mixed $value
     * @return string
     * @author Metizsoft Solutions
     */
    public function getOptionText($value)
    {
        $options = $this->getOptionsArray();
        if (!is_array($value)) {
            $value = explode(',', $value);
        }
        $texts = array();
        foreach ($value as $v) {
            if (isset($options[$v])) {
                $texts[] = $options[$v];
            }
        }
        return implode(', ', $texts);
    }
}
